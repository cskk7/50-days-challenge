
public class PrintElements {
    public static void main(String[] args) {
        
        int[] arr = {10,20,30,40,5060,70,80,90,100};
        // Printing the array elements using for loop iteration
        for(int i = 0; i < arr.length; i++){
            System.out.print(arr[i] + " ");
        }
        
    }
}
